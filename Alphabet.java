
public class Alphabet {
    public static void main(String[] args) {
        char c = 'd';
        if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')) {
            System.out.println(c + " = This character is  Alphabet.");
        } else {
            System.out.println(c + " = This character is not Alphabet.");
        }

    }
}