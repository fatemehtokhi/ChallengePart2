public class QuotientAndRemainder{
    public static void main(String[] args) {
        int divided =255;
        int divisor = 5;
        int quotient;
        int remainder;
        quotient  = divided/divisor;
        remainder = divided%divisor;
        System.out.println("Quotient is = "+quotient);
        System.out.println("Remainder is ="+remainder);
        
    }

}